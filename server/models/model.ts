// Require in mongoose
const mongoose = require('mongoose');

// Create a schema
const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  username: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
});

// Create model
const User = mongoose.model('User', userSchema);

module.exports = User;
